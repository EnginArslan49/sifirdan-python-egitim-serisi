# Değişken: Bilgiyi saklamak için kullandığımız isimlendirilmiş alan.
# Bir değişkene değer atayabilir, sonra bu değeri kullanabilir veya değiştirebiliriz.

# Örnekler:

sayi = 10          # "sayi" değişkeninde 10 saklanıyor
isim = "Ahmet"     # "isim" değişkeninde bir metin (string) saklanıyor
fiyat = 19.99      # "fiyat" değişkeninde ondalıklı sayı (float) saklanıyor

# Değişkenin değeri değiştirilebilir:
sayi = 15          # "sayi" artık 15 değerini tutuyor

# Değişkenler hesaplamalarda kullanılabilir:
toplam = sayi + 5  # sayi + 5 işleminin sonucu "toplam" değişkenine atanıyor

# Değişkenler, programın ilerleyen kısımlarında tekrar kullanılabilir:
print(isim)        # Ekrana "Ahmet" yazdırır
print(toplam)      # Ekrana 20 yazdırır
print(sayi)       # Ekrana 15 yazdırır (Başta değer 10 iken daha sonradan 10 olan bu değeri satır 11'de değeri 15 olarak değiştirdik.
