# Değişken tanımlama: Programda bir isim verip, ona bir değer atama işlemidir.
# Yani bir "kutu" oluşturup içine veri koymak gibidir.

# Genel şekli: değişken_adı = değer

# Örnekler:
sayi = 10           # "sayi" adında bir değişken oluşturduk ve içine 10 koyduk
isim = "Ayşe"       # "isim" adında bir değişken oluşturduk ve içine metin koyduk
fiyat = 25.50       # "fiyat" adında bir değişken oluşturduk ve içine ondalıklı sayı koyduk


