
'''
    Python’da değişken isimleri büyük/küçük harf duyarlıdır,
    yani aynı harflerden oluşsa bile harflerin büyük veya küçük olmasına
    göre farklı değişkenler olarak kabul edilir.
'''

# Örnek:
sayi = 10        # küçük harfli 'sayi'
Sayi = 20        # büyük harfli 'Sayi'
SAYI = 30        # tamamen büyük harfli 'SAYI'

# Hepsi farklı değişkenlerdir
print(sayi)  # 10
print(Sayi)  # 20
print(SAYI)  # 30
