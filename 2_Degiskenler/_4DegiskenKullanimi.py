# Değişken Tanımlama Örnekleri

# 1. Sayı tutan değişkenler
sayi = 10             # integer
puan = 95
yas = 25

# 2. Metin tutan değişkenler (string)
isim = "Ahmet"
soyisim = "Kaya"
sehir = "İstanbul"

# 3. Ondalıklı sayı tutan değişkenler (float)
fiyat = 19.99
agirlik = 72.5
oran = 0.15

# 4. Mantıksal değer tutan değişkenler (boolean)
aktif_mi = True
girdi_mi = False

# 5. Birden fazla değişkeni aynı anda tanımlamak
x, y, z = 5, 10, 15

# 6. Değişkeni sonradan değiştirmek
sayi = 10
sayi = sayi + 5   # artık sayi = 15

# 7. Değişkenleri birbirine eşitlemek
a = b = c = 20    # a, b ve c = 20

# 8. Anlamlı değişken isimleri ile iyi kullanım
ogrenci_sayisi = 30
urun_fiyati = 120.50
islem_basarili = True
