

# 1. SAYILAR (Numbers)
# Tam sayılar (int)
yas = 25

# Ondalıklı sayılar (float)
fiyat = 19.99

# 2. METİNLER (Strings)
isim = "Ahmet"

# 3. TEK KARAKTERLER (Char)
karakter = 'E'

# 4. MANTIKSAL DEĞERLER (Boolean)
aktif_mi = True
giris_yapti_mi = False

# 5. LİSTELER (List) - Birden fazla veriyi bir arada saklar, değiştirilebilir
meyveler = ["elma", "armut", "muz"]

# 6. DEMETLER (Tuple) - Listeler gibi ama değiştirilemez
renkler = ("kırmızı", "mavi", "yeşil")

# 7. KÜMELER (Set) - Tekil değerleri saklar, sırasızdır
sayilar = {1, 2, 3, 4}

# 8. SÖZLÜKLER (Dictionary) - Anahtar-değer çiftleri tutar
kisi = {"isim": "Ahmet", "yas": 25}

# ==============================
# Özet:
# int, float, complex → sayılar
# str → metinler
# char → karakterler
# bool → doğru/yanlış
# list, tuple, set, dict → birden fazla veri saklamak için kullanılır
# ==============================
