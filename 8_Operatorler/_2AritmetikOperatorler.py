
# Aritmetik operatörler sayısal işlemler yapmak için kullanılır.
# Toplama, çıkarma, çarpma, bölme gibi temel matematiksel işlemleri yapar.

x = 10
y = 3

# Toplama (+)
print("Toplama:", x + y)        # 10 + 3 = 13

# Çıkarma (-)
print("Çıkarma:", x - y)        # 10 - 3 = 7

# Çarpma (*)
print("Çarpma:", x * y)         # 10 * 3 = 30

# Bölme (/)
print("Bölme:", x / y)          # 10 / 3 = 3.3333...

# Tam bölme (//)
print("Tam Bölme:", x // y)     # 10 // 3 = 3 (ondalık kısmı atar)

# Mod alma (%)
print("Kalan (Mod):", x % y)    # 10 % 3 = 1 (bölmeden kalan)

# Üs alma (**)
print("Üs Alma:", x ** y)       # 10 ** 3 = 1000 (10 üzeri 3)
