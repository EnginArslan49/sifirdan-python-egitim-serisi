
# Karşılaştırma operatörleri, iki değeri karşılaştırır ve sonucu True (doğru) veya False (yanlış) olarak döndürür.
# Bu operatörler genellikle bir sonraki bölümde göreceğimiz koşul ifadelerinde (if, while vb.) kullanılır.

a = 10
b = 5

# ==  → Eşit mi?
print("a == b :", a == b)   # False, çünkü 10 eşit değildir 5'e

# !=  → Eşit değil mi?
print("a != b :", a != b)   # True, çünkü 10 eşit değildir 5'e

# >   → Büyük mü?
print("a > b  :", a > b)    # True, çünkü 10 büyüktür 5'ten

# <   → Küçük mü?
print("a < b  :", a < b)    # False, çünkü 10 küçük değildir 5'ten

# >=  → Büyük veya eşit mi?
print("a >= b :", a >= b)   # True, çünkü 10 büyüktür 5'ten

# <=  → Küçük veya eşit mi?
print("a <= b :", a <= b)   # False, çünkü 10 ne küçük ne de eşit 5'e